#include "../includes/libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	char	c;

	if (n == -2147483648)
		return (ft_putstr_fd("-2147483648", fd));
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		n *= -1;
	}
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		n = n % 10;
	}
	if (n < 10)
	{
		c = '0' + n;
		ft_putchar_fd(c, fd);
	}
}

void	ft_putnbr(int n)
{
	ft_putnbr_fd(n, 1);
}
